// Na linguagem Java, qualquer código que escrevemos
// fica dentro de uma estrutura chamada 'classe'
public class PrimeiraClasse {
    // O método 'main' é o ponto de partida do projeto,
    // é por onde a aplicação irá começar
    public static void main(String[] args) {
        System.out.println("Hello world!!!");
        System.out.println("Odeio o Roger Guedes");

        // Tipos mais utilizados
        Integer numeroInteiro = 1;
        int numeroPrimitivo = 1;

        Double numeroReal = 0.0;
        String texto = "Murilo Marconi";
        Boolean bloqueado = true;

        // Tipos menos utilizados
        Float numeroReal2 = 1.0F;
        Long numeroMuitoGrande = 11L;
        Character bloqueado2 = 'N';
    }
}
